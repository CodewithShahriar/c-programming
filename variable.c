#include <stdio.h>

int main()
{
    // declaration with definition
    int playerScore ;

    printf("playerScore: %d\n", playerScore);
    
    // initialization
    playerScore = 93 ;


    // declaration + definition + initialization
    int player = 95 ;

    
    printf("value of playerScore after initialization: %d\n", playerScore);
    printf("value of player: %d", player);

    return 0;
}